#[derive(Debug, Default)]
pub struct Coords {
  pub x: f64,
  pub y: f64
}

#[derive(Debug, Default)]
pub struct CoordsZ {
  pub x: f64,
  pub y: f64,
  pub z: f64
}

#[derive(Debug, Default)]
pub struct CoordsM {
  pub x: f64,
  pub y: f64,
  pub m: f64
}

#[derive(Debug, Default)]
pub struct CoordsZM {
  pub x: f64,
  pub y: f64,
  pub z: f64,
  pub m: f64
}
